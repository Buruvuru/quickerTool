#function to set the Doctype
def doctypeTag():
    tag="<!DOCTYPE html>"
    return tag

#opening html tag
def openingHtmlTag():
    tag="<html>"
    return tag

#closing html tag
def closingHtmlTag():
    tag="</html>"
    return tag

#html opening head tag
def openingHeadTag():
    tag="<head>"
    return tag

#html closing head tag
def closingHeadTag():
    tag="</head>"
    return tag
